class Estudiante {
    String carnet;
    String nombresApellidos;
    double primerParcial;
    double segundoParcial;
    double proyecto;
    double sistematicos;

    public Estudiante(String carnet, String nombresApellidos, double primerParcial, double segundoParcial, double proyecto, double sistematicos) {
        this.carnet = carnet;
        this.nombresApellidos = nombresApellidos;
        this.primerParcial = primerParcial;
        this.segundoParcial = segundoParcial;
        this.proyecto = proyecto;
        this.sistematicos = sistematicos;
    }
}
